
public class PiramideArriba {
	 public static void main(String[] args) {
	        int filas = 5; 

	        for (int i = 1; i <= filas; i++) {

	            for (int j = 1; j <= i; j++) {
	                System.out.print(" * ");
	            }
	            System.out.println();
	        }
	    }
	}

 